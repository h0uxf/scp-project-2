//////////////////////////////////////////////////////
// REQUIRED MODULES
//////////////////////////////////////////////////////
const express = require('express');

//////////////////////////////////////////////////////
// IMPORT CONTROLLERS
//////////////////////////////////////////////////////
const adminController = require('../controllers/adminController.js');

//////////////////////////////////////////////////////
// IMPORT MIDDLEWARES FOR INPUT VALIDATION
//////////////////////////////////////////////////////
const { sanitizeRequest, sanitizeResponse } = require('../middlewares/sanitizers.js');
const { validate, getAllUsersValidationRules } = require('../middlewares/validators.js');

//////////////////////////////////////////////////////
// CREATE ROUTER
//////////////////////////////////////////////////////
const router = express.Router();
router.use(sanitizeRequest); 

//////////////////////////////////////////////////////
// DEFINE ROUTES FOR MANAGING USERS (ADMIN)
//////////////////////////////////////////////////////
// [GET] Get all users
router.get(
    '/users',
    getAllUsersValidationRules(),
    validate,
    adminController.getAllUsers
);

// [GET] Get user by ID
router.get(
    '/users/:userId',
    adminController.getUserById
);

// [PUT] Update user role by ID
router.put(
    '/users/:userId',
    adminController.updateUserById
);

// [DELETE] Delete user by ID
router.delete(
    '/users/:userId',
    adminController.deleteUserById
);

//////////////////////////////////////////////////////
// DEFINE ROUTES FOR USER STATISTICS (ADMIN)
//////////////////////////////////////////////////////
// [GET] Get user statistics
router.get(
    '/statistics',
    adminController.getUserStatistics
);

//////////////////////////////////////////////////////
// EXPORT ROUTER
//////////////////////////////////////////////////////
router.use(sanitizeResponse);
module.exports = router;